/**
 * Copies images from Photos/The Practice/<folder>/ into
 * public/images/practice/<slug>/01.jpg, 02.jpg, ... (order = filename numbering),
 * copies Photos/The Practice/Cover Page.jpg → public/images/practice-series-cover.jpg
 * (Art Gallery hub — same idea as Takhleeq’s takhleeq-series-cover.jpg),
 * and writes src/data/practiceImages.generated.ts
 *
 * Run from repo root: npm run sync-practice
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

/** [Photos folder name, URL slug under /images/practice/<slug>/] */
const ENTRIES = [
  ["Golden Hour", "golden-hour"],
  ["The Insistence of Red", "insistence-of-red"],
];

const PHOTOS = path.join(root, "Photos", "The Practice");
const OUT_BASE = path.join(root, "public", "images", "practice");
const GEN = path.join(root, "src", "data", "practiceImages.generated.ts");
const HUB_COVER_SRC = path.join(PHOTOS, "Cover Page.jpg");
const HUB_COVER_DEST = path.join(root, "public", "images", "practice-series-cover.jpg");

const IMAGE_EXT = /\.(jpe?g|png|webp)$/i;

function isUuidLikeStem(stem) {
  return /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i.test(
    stem
  );
}

function sortKey(filename) {
  const stem = path.parse(filename).name.trim();
  const lead = stem.match(/^(\d+)/);
  if (lead) {
    return { tier: 0, n: parseInt(lead[1], 10), stem };
  }
  if (isUuidLikeStem(stem)) {
    return { tier: 2, n: 0, stem };
  }
  const any = stem.match(/(\d+)/);
  if (any) {
    return { tier: 1, n: parseInt(any[1], 10), stem };
  }
  return { tier: 2, n: 0, stem };
}

function sortPaintingPhotos(names) {
  return [...names].sort((a, b) => {
    const ka = sortKey(a);
    const kb = sortKey(b);
    if (ka.tier !== kb.tier) return ka.tier - kb.tier;
    if (ka.n !== kb.n) return ka.n - kb.n;
    return ka.stem.localeCompare(kb.stem, undefined, {
      numeric: true,
      sensitivity: "base",
    });
  });
}

function listImageFiles(dir) {
  if (!fs.existsSync(dir)) {
    console.warn(`Missing folder: ${dir}`);
    return [];
  }
  const names = fs
    .readdirSync(dir, { withFileTypes: true })
    .filter((d) => d.isFile() && IMAGE_EXT.test(d.name))
    .map((d) => d.name);
  return sortPaintingPhotos(names);
}

function ensureDir(d) {
  fs.mkdirSync(d, { recursive: true });
}

function extFor(srcName) {
  const m = srcName.match(/(\.[^.]+)$/i);
  return (m ? m[1] : ".jpg").toLowerCase();
}

const record = {};

for (const [folderName, slug] of ENTRIES) {
  const srcDir = path.join(PHOTOS, folderName);
  const names = listImageFiles(srcDir);
  const destDir = path.join(OUT_BASE, slug);
  ensureDir(destDir);

  const urls = [];
  names.forEach((name, i) => {
    const ext = extFor(name);
    const seq = String(i + 1).padStart(2, "0");
    const destName = `${seq}${ext}`;
    const destPath = path.join(destDir, destName);
    fs.copyFileSync(path.join(srcDir, name), destPath);
    urls.push(`/images/practice/${slug}/${destName}`);
  });

  if (!urls.length) {
    console.warn(`No images for Practice: ${folderName} (${slug})`);
  }
  record[slug] = urls;
}

const lines = Object.keys(record)
  .sort()
  .map((k) => {
    const arr = record[k].map((u) => `    "${u.replace(/\\/g, "/")}",`).join("\n");
    return `  "${k}": [\n${arr}\n  ],`;
  });

const fileBody = `// Generated by scripts/sync-practice-images.mjs — do not edit by hand
export const practiceImagesBySlug: Record<string, string[]> = {
${lines.join("\n")}
};
`;

fs.writeFileSync(GEN, fileBody, "utf8");
console.log(`Wrote ${GEN}`);
console.log(`Copied images under ${OUT_BASE}`);

if (fs.existsSync(HUB_COVER_SRC)) {
  fs.mkdirSync(path.dirname(HUB_COVER_DEST), { recursive: true });
  fs.copyFileSync(HUB_COVER_SRC, HUB_COVER_DEST);
  console.log(`Hub cover: ${HUB_COVER_SRC} → ${HUB_COVER_DEST}`);
} else {
  console.warn(`Missing hub cover (optional): ${HUB_COVER_SRC}`);
}
