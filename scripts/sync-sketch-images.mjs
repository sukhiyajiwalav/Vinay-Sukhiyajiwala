/**
 * Copies images from Photos/Sketch/<folder>/ into
 * public/images/sketch/<slug>/01.jpg, 02.jpg, ... (order = filename numbering),
 * copies Photos/Sketch/Cover Page.jpg → public/images/sketch-series-cover.jpg,
 * and writes src/data/sketchImages.generated.ts + src/data/sketches.generated.ts
 *
 * Run from repo root: npm run sync-sketch
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

const PHOTOS = path.join(root, "Photos", "Sketch");
const OUT_BASE = path.join(root, "public", "images", "sketch");
const HUB_COVER_SRC = path.join(PHOTOS, "Cover Page.jpg");
const HUB_COVER_DEST = path.join(root, "public", "images", "sketch-series-cover.jpg");
const GEN_IMAGES = path.join(root, "src", "data", "sketchImages.generated.ts");
const GEN_SKETCHES = path.join(root, "src", "data", "sketches.generated.ts");

const IMAGE_EXT = /\.(jpe?g|png|webp)$/i;

function isUuidLikeStem(stem) {
  return /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i.test(
    stem
  );
}

function sortKey(filename) {
  const stem = path.parse(filename).name.trim();
  const lead = stem.match(/^(\d+)/);
  if (lead) return { tier: 0, n: parseInt(lead[1], 10), stem };
  if (isUuidLikeStem(stem)) return { tier: 2, n: 0, stem };
  const any = stem.match(/(\d+)/);
  if (any) return { tier: 1, n: parseInt(any[1], 10), stem };
  return { tier: 2, n: 0, stem };
}

function sortPhotos(names) {
  return [...names].sort((a, b) => {
    const ka = sortKey(a);
    const kb = sortKey(b);
    if (ka.tier !== kb.tier) return ka.tier - kb.tier;
    if (ka.n !== kb.n) return ka.n - kb.n;
    return ka.stem.localeCompare(kb.stem, undefined, {
      numeric: true,
      sensitivity: "base",
    });
  });
}

function ensureDir(d) {
  fs.mkdirSync(d, { recursive: true });
}

function listImageFiles(dir) {
  if (!fs.existsSync(dir)) return [];
  const names = fs
    .readdirSync(dir, { withFileTypes: true })
    .filter((d) => d.isFile() && IMAGE_EXT.test(d.name))
    .map((d) => d.name);
  return sortPhotos(names);
}

function extFor(srcName) {
  const m = srcName.match(/(\.[^.]+)$/i);
  return (m ? m[1] : ".jpg").toLowerCase();
}

function slugify(name) {
  return name
    .trim()
    .toLowerCase()
    .replace(/['"]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "");
}

/** Folder names under Photos/Sketch (excluding the cover file) */
function listSketchFolders() {
  if (!fs.existsSync(PHOTOS)) return [];
  return fs
    .readdirSync(PHOTOS, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" }));
}

function shuffleInPlace(arr) {
  // Fisher–Yates
  for (let i = arr.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

function orderedSketchFolders() {
  const all = listSketchFolders();
  const SOCIETY = "Society";
  const BABY_BOY = "Baby Boy";
  const BOY = "Boy";

  const middle = all.filter((n) => n !== SOCIETY && n !== BABY_BOY && n !== BOY);
  shuffleInPlace(middle);

  const out = [];
  if (all.includes(SOCIETY)) out.push(SOCIETY);
  // Keep one random item in slot 2 (if any exist).
  if (middle.length) out.push(middle.shift());
  // Force Boy into slot 3 when present.
  if (all.includes(BOY)) out.push(BOY);
  // Remaining shuffled items.
  out.push(...middle);
  // Force Baby Boy to be last when present.
  if (all.includes(BABY_BOY)) out.push(BABY_BOY);
  return out;
}

const imagesBySlug = {};
const sketches = [];

ensureDir(OUT_BASE);

for (const folderName of orderedSketchFolders()) {
  const slug = slugify(folderName);
  const srcDir = path.join(PHOTOS, folderName);
  const names = listImageFiles(srcDir);
  const destDir = path.join(OUT_BASE, slug);
  ensureDir(destDir);

  const urls = [];
  names.forEach((name, i) => {
    const ext = extFor(name);
    const seq = String(i + 1).padStart(2, "0");
    const destName = `${seq}${ext}`;
    fs.copyFileSync(path.join(srcDir, name), path.join(destDir, destName));
    urls.push(`/images/sketch/${slug}/${destName}`);
  });

  imagesBySlug[slug] = urls;
  sketches.push({ slug, title: folderName });
}

const linesImages = Object.keys(imagesBySlug)
  .sort()
  .map((k) => {
    const arr = imagesBySlug[k]
      .map((u) => `    "${u.replace(/\\/g, "/")}",`)
      .join("\n");
    return `  "${k}": [\n${arr}\n  ],`;
  });

const fileBodyImages = `// Generated by scripts/sync-sketch-images.mjs — do not edit by hand
export const sketchImagesBySlug: Record<string, string[]> = {
${linesImages.join("\n")}
};
`;

const fileBodySketches = `// Generated by scripts/sync-sketch-images.mjs — do not edit by hand
export const sketchesMeta: { slug: string; title: string }[] = [
${sketches
  .map((s) => `  { slug: "${s.slug}", title: "${s.title.replace(/\\/g, "\\\\").replace(/"/g, '\\"')}" },`)
  .join("\n")}
];
`;

fs.writeFileSync(GEN_IMAGES, fileBodyImages, "utf8");
fs.writeFileSync(GEN_SKETCHES, fileBodySketches, "utf8");
console.log(`Wrote ${GEN_IMAGES}`);
console.log(`Wrote ${GEN_SKETCHES}`);
console.log(`Copied images under ${OUT_BASE}`);

if (fs.existsSync(HUB_COVER_SRC)) {
  fs.mkdirSync(path.dirname(HUB_COVER_DEST), { recursive: true });
  fs.copyFileSync(HUB_COVER_SRC, HUB_COVER_DEST);
  console.log(`Hub cover: ${HUB_COVER_SRC} → ${HUB_COVER_DEST}`);
} else {
  console.warn(`Missing hub cover (optional): ${HUB_COVER_SRC}`);
}
